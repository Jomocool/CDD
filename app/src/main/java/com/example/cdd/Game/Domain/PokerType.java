package com.example.cdd.Game.Domain;

public enum PokerType {
    cDefault,
    //单张：任何一张单牌
    c1,
    //一对：二张牌点相同的牌
    c2,
    //三个：三张牌点相同的牌
    c3,
    //杂顺：花色不全相同的顺
    c11111,
    //同花顺：每张牌的花色都相同的顺称为同花顺
    c11111Five,
    //同花五：由相同花色的五张牌组成，但不是顺
    cFive,
    //三个带一对：三张牌点相同的牌带两张牌点相同的牌
    c32,
    //四个带单张：四张牌点相同的牌带任意一张单牌
    c41,
    //不符合规则的牌型
    cError
}
